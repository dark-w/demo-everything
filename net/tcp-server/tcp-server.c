#define M_OK 0
#define M_ERR_CRC 1
#define M_ERR_ADR 2
#define M_ERR_OPE 3
#define M_ERR_LEN 4

static int modbus_handle(int client_fd, const uint8_t *datagram, uint32_t len)
{
    if (len < 4)
        return M_ERR_LEN;

    const uint16_t crc = (datagram[len - 2] << 8) + datagram[len - 1];
    if (crc16(datagram, len - 2) != crc)
        return M_ERR_CRC;

    if (datagram[0] != dev_addr)
        return M_ERR_ADR;

    const uint8_t operate = datagram[1];
    const uint16_t addr   = (datagram[2] << 8) + datagram[3];
    switch (operate) {
        case 0x03:
            for (uint8_t i = 0; i < sizeof(mr_list) / sizeof(mr_list[0]); i++) {
                if (addr == mr_list[i].addr) {
                    uint16_t retval;
                    if (MR_DT_UINT16 == mr_list[i].data_type)
                        retval = *(uint16_t *)mr_list[i].data;
                    else if (MR_DT_UINT8 == mr_list[i].data_type)
                        retval = (uint16_t)(*(uint8_t *)mr_list[i].data);

                    retval = (retval >> 8) + (retval << 8);

                    send(client_fd, &retval, sizeof(retval), 0);
                }
            }
            break;

        case 0x06:
            break;

        default:
            return M_ERR_OPE;
    }

    return M_OK;
}
