static uint16_t crc16(const uint8_t *pdata, uint16_t len)
{
    uint8_t crc_high  = 0xFF;
    uint8_t crc_low   = 0xFF;
    uint8_t crc_index = 0;
  
    while (len--) {
        crc_index = crc_high ^ *pdata++;
        crc_high  = crc_low ^ crc_table_high[crc_index];
        crc_low   = crc_table_low[crc_index];
    }

    return ((uint16_t)crc_high << 8) | ((uint16_t)(crc_low));
}

